import Head from "next/head";
import Header from "@/components/Header/Header";
import { useState } from "react";
import { iFilters } from "@/interfaces";
import CardContainer from "@/components/Card/CardContainer";
import FiltersBar from "@/components/Filter/FiltersBar";
import CreateTaskButton from "@/components/CreateTaskButton";

export default function Home() {
  const [filtersBarState, setFiltersBarState] = useState<boolean>(true);
  const [layoutMode, setLayoutMode] = useState("grid");
  const [filters, setFilters] = useState<iFilters>({
    inputValue: "",
    section: "all",
    orderBy: 0,
  });

  const changeFiltersBarState = () => setFiltersBarState(!filtersBarState);
  const changeFilters = (filterName: string, value: string | number) =>
    setFilters({ ...filters, [filterName]: value });

    const [source, setSource] = useState<string>("")

    const handleCapture = ({target}: React.ChangeEvent<HTMLInputElement>) => {
      if (target.files) {
        if (target.files.length !== 0) {
          const file = target.files[0];
          const newUrl = URL.createObjectURL(file);
          setSource(newUrl);
        }
      }
    }

  return (
    <>
      <Head>
        <title>TS-Tasks-App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="min-h-screen relative z-40">
        <section className="z-40 sticky top-0 bg-secondaryWhite dark:bg-secondaryBlack">
          <Header
            filtersBarState={filtersBarState}
            changeFiltersBarState={changeFiltersBarState}
            changeFilters={changeFilters}
          />

          {filtersBarState && (
            <FiltersBar
              changeFilters={changeFilters}
              changeLayout={setLayoutMode}
              layoutMode={layoutMode}
            />
          )}
        </section>

        <section className="max-w-7xl w-full mx-auto lg:px-4">
          <CardContainer
            filters={filters}
            filtersBarState={filtersBarState}
            layoutMode={layoutMode}
          />

          <input
            accept="image/*"
            id="icon-button-file"
            type="file"
            capture="environment"
            onChange={(e) => handleCapture(e)}
          />    
          
        </section>
        
        <div className="flex flex-col items-center right-0 bottom-0 fixed">
          <CreateTaskButton mobileMode />
        </div>
      </main>
    </>
  );
}
